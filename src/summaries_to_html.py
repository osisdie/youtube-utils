def build_html(channel_dir: Path, lang: str) -> str:
    """Collect all summary files and build a single HTML document."""
    suffix = f"summary_{lang}.md"
    video_dirs = sorted(
        [d for d in channel_dir.iterdir() if d.is_dir() and (d / suffix).exists()]
    )

    if not video_dirs:
        print(f"No {suffix} files found in {channel_dir}", file=sys.stderr)
        sys.exit(1)

    print(f"Found {len(video_dirs)} videos with {suffix}")

    lang_attr = "zh-Hant" if "zh" in lang else "en"
    title = (
        f"{channel_dir.name} — {'影片摘要集' if 'zh' in lang else 'Video Summaries'}"
    )
    toc_title = "目錄" if "zh" in lang else "Table of Contents"

    toc_items = []
    sections = []
    md_ext = markdown.Markdown(extensions=["tables", "fenced_code", "toc"])

    for i, vdir in enumerate(video_dirs, 1):
        md_text = (vdir / suffix).read_text(encoding="utf-8")

        # Extract title from first H1
        first_line = md_text.split("\n")[0]
        video_title = re.sub(r"^#+\s*", "", first_line).strip()
        if not video_title:
            video_title = vdir.name

        anchor = f"video-{i}"
        toc_items.append(f'<li><a href="#{anchor}">{video_title}</a></li>')

        # Embed images as base64
        md_text = _resolve_images(md_text, vdir)

        md_ext.reset()
        html_body = md_ext.convert(md_text)

        sections.append(
            f'<div class="video-section" id="{anchor}">\n{html_body}\n</div>'
        )
        print(f"  [{i}/{len(video_dirs)}] {video_title[:60]}")

    return HTML_TEMPLATE.format(
        lang_attr=lang_attr,
        title=title,
        toc_title=toc_title,
        toc_items="\n".join(toc_items),
        sections="\n".join(sections),
    )


def main():
    parser = argparse.ArgumentParser(
        description="Aggregate video summaries into a single HTML document"
    )
    parser.add_argument(
        "channel_dir", help="Channel output directory (e.g. output/youtube/cfoooo8337)"
    )
    parser.add_argument(
        "--lang",
        default="zh-tw",
        help="Summary language suffix (default: zh-tw). Looks for summary_{lang}.md",
    )
    parser.add_argument(
        "-o",
        "--output",
        default=None,
        help="Output HTML path (default: {channel_dir}/summaries_{lang}.html)",
    )
    args = parser.parse_args()

    channel_dir = Path(args.channel_dir)
    if not channel_dir.is_dir():
        print(f"Error: {channel_dir} is not a directory", file=sys.stderr)
        sys.exit(1)

    html = build_html(channel_dir, args.lang)

    out_path = (
        Path(args.output)
        if args.output
        else channel_dir / f"summaries_{args.lang}.html"
    )
    out_path.write_text(html, encoding="utf-8")
    print(f"\nHTML written to: {out_path} ({out_path.stat().st_size / 1024:.0f} KB)")


if __name__ == "__main__":
    main()
