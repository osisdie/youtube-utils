#!/usr/bin/env python3
"""
Aggregate per-video summary markdown files into a single styled HTML document.

Usage:
  python src/summaries_to_html.py output/youtube/cfoooo8337
  python src/summaries_to_html.py output/youtube/cfoooo8337 --lang en
  python src/summaries_to_html.py output/youtube/cfoooo8337 --lang zh-tw -o channel_book.html
"""

import argparse
import base64
import mimetypes
import re
import sys
from pathlib import Path

import markdown


def _image_to_data_uri(img_path: Path) -> str:
    """Convert a local image file to a base64 data URI for embedding in HTML."""
    mime, _ = mimetypes.guess_type(str(img_path))
    mime = mime or "image/jpeg"
    data = base64.b64encode(img_path.read_bytes()).decode()
    return f"data:{mime};base64,{data}"


def _resolve_images(md_text: str, video_dir: Path) -> str:
    """Replace relative image paths in markdown with base64 data URIs."""

    def _replace(match):
        alt = match.group(1)
        src = match.group(2)
        img_path = video_dir / src
        if img_path.exists():
            data_uri = _image_to_data_uri(img_path)
            return f"![{alt}]({data_uri})"
        return match.group(0)

    return re.sub(r"!\[([^\]]*)\]\(([^)]+)\)", _replace, md_text)


